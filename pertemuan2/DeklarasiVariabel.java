public class DeklarasiVariabel {
    // Tipe Data Integer
    static int angka = 100;
    
    // Tipe Data Double
    static double desimal = 3.14;
    
    // Tipe Data String
    static String nama = "Awalina Shifa Fi Yahya";
    
    // Tipe Data Boolean
    static boolean benar = true;
    
    // Tipe Data Char
    static char abjad = 'A';
    
    public static void main(String[] args) {
        System.out.println("Awalina Shifa Fi Yahya"); 
        System.out.println("Contoh Nilai Integer adalah : " + angka);
        System.out.println("Contoh Nilai Double adalah : " + desimal);
        System.out.println("Contoh Nilai String adalah : " + nama);
        System.out.println("Contoh Nilai Boolean adalah : " + benar);
        System.out.println("Contoh Nilai Char adalah : " + abjad);
    }
}

