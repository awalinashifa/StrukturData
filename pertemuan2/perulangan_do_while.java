public class perulangan_do_while {

    public static void main(String[] args) {
        int i = 1;

        System.out.println("Menampilkan angka 1 sampai 10 dengan do-while loop:");

        do {
            System.out.println("Angka ke-" + i);
            i++;
        } while (i <= 10);
    }
}
    

